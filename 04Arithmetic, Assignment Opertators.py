# Assignment Operators
a = 4-2 # Assign 4-2 to a
print(a) # Print the value of a

b = 6 # Assign 6 to b
b +=3 # Add(Incriment) 3 to b and then assign it to b
print(b) # Print the value of b

b -=3 # Subtract(Decrement) 3 from b and then assign it to b
print(b) # Print the value of b
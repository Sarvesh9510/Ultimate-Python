# Comparison Operators in Python
# Comparison operators are used to compare two values.
# They return a boolean value: True or False.

a = 5<4
print(a)
# This will print True because 5 is greater than 4

a = 5>=5
print(a)
# This will print True because 5 is equal to 5 and greater than or equal to 5

a = 5==7
print(a)
# This will print False because 5 is not equal to 7 

a = 5!=7
print(a)

a = input("Enter number 1: ")

b = input("Enter number 2: ")

print("Number a is:", a)
print("Number b is:", b)
# This will print the values of a and b entered by the user

# This is how strings are sum in Python
print("Sum of a and b is:", a + b)
# This will print the concatenation of a and b as strings

# if we want to perform arithmetic operations, we need to convert the strings to integers or floats
a = int(a)  # Convert string to integer
b = int(b)  # Convert string to integer
print("Sum of a and b as integers is:", a + b)
# This will print the sum of a and b as integers
